# type () and str ()  functions
# Allows you to find out the data type of a piece of data to use it wherever you want to use it within parenthesis

ex_1 = False
ex_2 = 29
ex_3 = 4.1352

print(type(ex_1))
print(type(ex_2))
print(type(ex_3))

# str () function : the String function
ex_4 = str(True)
ex_5 = str(5)
ex_6 = str(3.21)
print(type(ex_4))
print(type(ex_5))
print(type(ex_6))