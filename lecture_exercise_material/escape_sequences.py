# Escape sequences are the special characters you can
# use in strings which enables you to do things such as insert quotes within them
#  and and to make them appear on different line of the output
# \t (tab) is used to make a horizontal space while working with strings

print("This\tis\ta\tlot\tof\tspace.")

#\n new line character
print("line one\nline two")

# to put quotes in your string we use   \' and \"
print('"When I said \'immediately,\' I meant today!" said King Saul. ')
print("\"Do or do not. There is no try.\"")

# \\ the backslash escape sequence is used whenever you want to use a backslash in a string
print("All escape sequences contains a \\.")