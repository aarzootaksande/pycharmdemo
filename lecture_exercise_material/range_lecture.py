# range lecture
# using rage with 1 argument
one_input = range(3)
print("using rage with 1 argument")
for num in one_input:
    print(num)

# using rage with 2 argument
two_input = range(5, 10)
print("using rage with 2 argument")
for num in two_input:
    print(num)

# using rage with 3 argument
three_input = range(1, 10, 2)
print("using rage with 3 argument")
for num in three_input:
    print(num)